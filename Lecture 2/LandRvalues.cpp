/*
Lvalue (Locator Value):
An Lvalue refers to an expression that has a memory location (i.e., it can be addressed).

It typically appears on the left-hand side of an assignment operator (=).

Lvalues are persistent; they exist beyond the expression in which they are used.


Rvalue (Right Value):
An Rvalue refers to a temporary value that does not have a persistent memory location.

It typically appears on the right-hand side of an assignment operator (=).

Rvalues are short-lived and often represent intermediate results or literals.
*/